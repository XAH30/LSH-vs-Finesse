// Функция iloc предназначена для извлечения строки или столбца из двумерного вектора data на основе указанного индекса и оси.

// Аргументы функции:

// data - двумерный вектор, из которого нужно извлечь элементы.
// index - индекс строки или столбца, который нужно извлечь.
// axis - ось, по которой нужно произвести извлечение (0 - строки, 1 - столбцы).

// Алгоритм функции:

// Создается пустой вектор result, который будет содержать извлеченные элементы.
// Если ось axis равна 0, то выполняется извлечение строки.
// Если индекс index находится в допустимом диапазоне (от 0 до размера data), то строка с индексом index присваивается вектору result.
// Если ось axis равна 1, то выполняется извлечение столбца.
// Если data не пустой и индекс index находится в допустимом диапазоне (от 0 до размера столбцов в data[0]), 
// то происходит итерация по каждой строке row в data.
// На каждой итерации значение элемента с индексом index в текущей строке добавляется в вектор result.
// В конце функция возвращает вектор result, содержащий извлеченные элементы.

template<typename Type>
std::vector<Type> iloc(const std::vector<std::vector<Type>>& data, int index, int axis) {
    std::vector<Type> result;

    if (axis == 0) {
        // Получение строки по индексу
        if (index >= 0 && index < data.size()) {
            result = data[index];
        }
    } else if (axis == 1) {
        // Получение столбца по индексу
        if (!data.empty() && index >= 0 && index < data[0].size()) {
            for (const auto& row : data) {
                result.push_back(row[index]);
            }
        }
    }

    return result;
}